package com.mini.purchase.pagegoodsinfo.data;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.Table;

import com.itour.etip.pub.frame.FrmData;

/**
 *〈page和商品信息的中间表实体对象〉
 * @author    侯杨
 *     2014.09.10
 * @see PageGoodsInfoData
 * @since vmaque1.5
 */
@Entity
@Table(name="MINI_PAGE_GOODSINFO")
public class PageGoodsInfoData extends FrmData{
    /**
     */
    private static final long serialVersionUID = 1203671837490567625L;
    /**
     * pageid
     */
    @Column(name="PAGEID")
    private String pageId;
    /**
     * 商品信息id
     */
    @Column(name="GOODSINFOID")
    private String goodsInfoId;
    /**
     * 
     *〈pageid的get方法〉<br>
     * @author 侯杨 <br> 
     *		   2014-9-10
     * @return  String
     */
    public String getPageId() {
        return pageId;
    }
    /**
     * 
     *〈pageid的set方法〉<br>
     * @author 侯杨 <br> 
     *         2014-9-10
     * @param  pageId
     */
    public void setPageId(String pageId) {
        this.pageId = pageId;
    }
    /**
     * 
     *〈商品信息id的get方法〉<br>
     * @author 侯杨 <br> 
     *         2014-9-10
     * @return  String
     */
    public String getGoodsInfoId() {
        return goodsInfoId;
    }
    /**
     * 
     *〈商品信息id的set方法〉<br>
     * @author 侯杨 <br> 
     *         2014-9-10
     * @param  goodsInfoId
     */
    public void setGoodsInfoId(String goodsInfoId) {
        this.goodsInfoId = goodsInfoId;
    }
    
    
    
}
