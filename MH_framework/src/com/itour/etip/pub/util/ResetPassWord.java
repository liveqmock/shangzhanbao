package com.itour.etip.pub.util;

public class ResetPassWord{
	
	/**
	 * 随即生成4位数字的字符串
	 * @return
	 */
	public static String resetPassWord(){
		String resetPassword = "";
		while(resetPassword.length()<4){
			resetPassword+=(int)(Math.random()*10); 
		}
		return resetPassword;
//		return "1234";
	}
}
